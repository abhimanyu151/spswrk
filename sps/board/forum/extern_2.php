		<link rel="alternate" href="http://spokensanskrit.de/board/forum/viewtopic.php?id=2624&amp;action=new"/>
		<summary type="html"><![CDATA[<p>Nice topic. Thank you. </p><p>The desire to modernize Sanskrit usage has been around for a while and only folks who have made some headway in that direction is Samskrita Bhararti (SB).&#160; As you have pointed out, their approach may not been perfect and that is not without a reason! </p><p>1) External Sandhi - Most Sanskrit prose published today from India has hardly any external Sandhi applied. You can see that in some of the short-story collections or novels from SB (e.g. व्यूहभेदः - collection of short stories 2007). Occasionally you will see a story or two in Chandamama which uses some sandhi exotica but we must realize that a these few are really targeted towards those who do want to exercise their Sandhi knowledge (e.g., Univ. students/graduates &amp; experts)</p><p>2) Dual forms - Complete elimination is not really an option. One cannot use plural form when referring to a pair of objects; that will violate grammar! The trick is to use &#039;a pair of&#039; and use singular form. Eg. जनद्वयं तत्र अस्ति । However we still have to live with आवाम्, भवन्तौ, etc. I cannot think of good clean way out of this fix :-(&#160; &#160;</p><p>3) Using कृतन्त form, namely कृतवान् as opposed to अकरोत्&#160; is not without reason. The standard form is not usable for all prefixes. E.g. Past tense of प्रक्षालयति cannot be अप्रक्षालयत् ; that is incorrect. However the कृतन्त forms of verbs is uniform and can be used for all verbs with or without prefixes.</p><p>From my view point, the key issues are synonyms and redundancy in Sanskrit. Sanskrit simply has too many of them (e.g., 200+ verbs to indicate &#039;go&#039;) and so it takes years before one has a sense of progress. This issue is a big-turn off for many folks, particularly the younger generation, who want to see some payback for their efforts as they progress through the course.</p><p>The issue of synonyms and redundancy in English language has been addressed already (http://ogden.basic-english.org/) , but we need something like this for Sanskrit so that we can get interested folks on board quickly. I started looking in that direction few months ago. If anyone is interested, we can discuss it further. This probably can be a good final-year project in Linguistics!</p>]]></summary>
