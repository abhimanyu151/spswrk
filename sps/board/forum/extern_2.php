		<title type="html"><![CDATA[Right pronounciation and spelling - ARJUN, GANESH, RAM]]></title>
		<link rel="alternate" href="http://spokensanskrit.de/board/forum/viewtopic.php?id=617&amp;action=new"/>
		<summary type="html"><![CDATA[<p>The actual/original/Sanskrit form has a short &#039;a&#039; at the end of these names followed by a discharge of air (from the lungs). E.g., &quot;ArjunaH&quot;</p><p>In some Indian languages the air-discharge is dropped (e.g., &#039;arjuna&#039; in Punjabi). In Hindi and some other Indian languages, the short &#039;a&#039; is also dropped. In some south Indian languages &#039;H&#039; is replaced with something else (i.e., &#039;arjunan&#039; in Tamil). (Sometimes these changes stem from rules that govern importation of foreign proper name into the local language)</p><p>When someone wants to indicate that he/she is referring to Sanskrit pronunciation of a name, he/she would use &quot;ArjunaH&quot; or &quot;Arjuna&quot;. You might encounter this practice when the write-up is associated directly or indirectly with something else written in Sanskrit.</p><p>There is nothing improper about these other forms; it all depends on the context in which the name appears.</p>]]></summary>
