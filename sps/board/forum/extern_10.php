		<title type="html"><![CDATA[Do NOT Edit validated entries]]></title>
		<link rel="alternate" href="http://spokensanskrit.de/board/forum/viewtopic.php?id=531&amp;action=new"/>
		<summary type="html"><![CDATA[<p>If there is an entry with source identified as &quot;mw&quot;, &quot;mylius&quot;, etc, it is a valid entry from a well known dictionary. This entry should NOT be edited any further.</p><p>If you think there is another way to express the same meaning in English, you must make a new entry for that word. </p><p>That is, if you think a Sanskrit word has a different meaning than what is already in the dictionary, first look thru traditional dictionaries such as Monier-Williams, Apte, etc (See the main page for links to these dictionaries) and see if this word already exists so that it can be indicated in the &#039;source&#039; widget in the entry. Do use the English text indicated in the traditional source so that later your entry can be validated by reviewers, when you create a new entry for this word.</p><p>If the entry does not exist in any traditional dictionary, that is OK - you can still make the entry as long as it is not some a deviant interpretation of the word in an another Indian language (this is quite common!).</p><p>That is how we generate synonyms for words in this dictionary.</p><p>Under no circumstances, please do NOT clobber validated entries that have been inserted after lots of efforts by many people.</p><p>Thank you.</p>]]></summary>
