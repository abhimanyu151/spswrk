		<content type="html"><![CDATA[<p>This is lit-lakara (लिट्लकार), which is used for past event which has not taken place in front of the speaker. <br />In &quot;normal&quot; dhaatus it causes repetition of first letter or similar &quot;echoing&quot; e.g.- पठ् causes पपाठ, भू causes बभूव, कृ causes चकार etc. <br />But for चुरादिगण and causative (ending with णिच्), repetition is not allowed and there are following ways to make forms in that case- चोरयांचकार (use कृ-forms e.g. चकार here, with original dhaatu), चोरयाम्बभूव (use भू-forms...), चोरयामास (use अस् form, as far as i understand it is अस् form only). So similarly गमयति (causes to go) in litlakaara becomes गमयामास while गम्, becomes जगाम (because first one has causation- णिच्).<br />Hence चोरयामास means, he/she stole (but I did not witness it).<br />गमयामास means, he/she drove (it). (but I did not witness it).</p><p>This is of course not a normal tense for most of other languages, but it does improve presentation skill of the speaker by giving subtlety of the situation.</p>]]></content>
