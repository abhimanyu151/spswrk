		<content type="html"><![CDATA[<p>As admin has correctly explained, the मदीय is used to describe things owned by me.<br />मदीयः अश्वः = my horse [the horse that I own]<br />मदीयं पुस्तकम् = my book [the book that I own]</p><p>मामक is an adjective derived from मम <br />It is used to describe people/things belonging to one&#039;s side.<br />E.g. मामकाः पान्डवाश्चैव किमकुर्वत संजय <br />This was spoken by Dhritarashtra to Sanjay.<br />Dhritarashtra is asking Sanjaya <br />&quot;What did my people/ people belonging to my side [i.e. Kaurava] and Pandava do&quot; ?</p><p>मामकीन is in turn an adjective derived from मामक <br />It is used to described a property of people/things belonging to my side.<br />धैर्यवंतः मामकीनाः = brave people belonging to my side</p><p>Please ask if more information is required.</p>]]></content>
