		<content type="html"><![CDATA[<p>Both rājan and brahman are nouns in their crude, undeclined form. When you need to use these words as the subject of a sentence you put them in the nominative case (rājā and brahmā respectively). When you wish instead to address someone, like Kṛṣṇa does with brahman in the Gītā, you need to use the vocative case, which usually coincides with the undeclined form of the noun. To sum it up:</p><p>he rājan -- O king!<br />rājā [...] -- the/a king [...]</p><p>Alex</p>]]></content>
