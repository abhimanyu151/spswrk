		<content type="html"><![CDATA[<p>Literally एतदपि गमिष्यति means &#039;This also will go&#039; However depending on the context it could mean &#039;This too shall pass&#039; (This phenomenon also will eventually disappear), &#039;This also can go&#039; (This bus can also go), etc.</p><p>If you are looking for clear and explicit statement, then सर्वम् अनित्यम् which means &#039;Everything is impermanent&#039; would be more appropriate.</p><p>So, it all depends on whether you want to be figurative or explicit and unambiguous. Based on the context both could mean the same.</p>]]></content>
