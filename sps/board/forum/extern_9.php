		<link rel="alternate" href="http://spokensanskrit.de/board/forum/viewtopic.php?id=2836&amp;action=new"/>
		<summary type="html"><![CDATA[<p>There have been some new updates this week:</p><p>1) All the [English] spelling errors have been rectified. That is, spelling now conforms to British/American/Canadian spelling.</p><p>2) All the Botanical and Zoological names have been identified with common names. There are a few unresolved ones still (my attempts to resolve these were not successful). If you encounter an unresolved one in the dictionary and know its common name and/or currently used Latin name for the species, let us know. Of course, I need some authoritative source to go with it - personally vouching for it wont help! :-)</p><p>3) All intra-word sandhi errors have been rectified. E.g., saMdhi -&gt; sandhi. While a good number of folks dont mind this, PANini does! (See परसवर्णसन्धिः in most Sanskrit grammar book.)</p><p>This has been slow and time-consuming task. I am sure there are still some stragglers which I missed. If you spot any, do let us know.</p><p>धन्यवादः</p>]]></summary>
